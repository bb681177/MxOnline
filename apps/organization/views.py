class AddFavView(View):
    """
    用户收藏开关
    fav_id与av_type两个参数，如果存在表示已经收藏，这时候传递表示是要取消，
    不存在表示是收藏。所以先要查询一下，这两个参数是否存在，为了判断用户是否存在，必须先判断用户是否登录
    """
    def post(self, request):
        fav_id = request.POST.get('fav_id', '0')
        fav_type = request.POST.get('fav_type', '')

        if not request.user.is_authenticated():
            # 判断用户登录状态
            return HttpResponse('{"status":"fail", "msg":"用户未登录"}', content_type='application/json')

        exist_records = UserFavorite.objects.filter(user=request.user, fav_id=int(fav_id), fav_type=int(fav_type))
        if exist_records:
            # 如果记录已经存在， 则表示用户是取消收藏
            exist_records.delete()
            return HttpResponse('{"status":"success", "msg":"收藏"}', content_type='application/json')
        else:
            user_fav = UserFavorite()
            if int(fav_id) > 0 and int(fav_type) > 0:
                user_fav.user = request.user
                user_fav.fav_id = int(fav_id)
                user_fav.fav_type = int(fav_type)
                user_fav.save()
                return HttpResponse('{"status": "success", "已收藏":}}', content_type='application/json')
            else:
                return HttpResponse('{"status": "fail", "收藏出错":}}', content_type='application/json')



